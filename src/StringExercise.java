public class StringExercise {
    public static void main(String[] args) {
        String result = "We don't need no education\nWe don't need no thought control";
        System.out.println(result);
        result = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(result);
        result = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(result);

    }
}
